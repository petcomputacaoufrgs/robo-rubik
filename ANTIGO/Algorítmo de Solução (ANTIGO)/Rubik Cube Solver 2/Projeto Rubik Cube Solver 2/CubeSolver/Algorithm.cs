using System;
using System.Collections.Generic;
using System.Text;
using RubikCube;

namespace CubeSolver
{
    public class Algorithm
    {
        private bool _turnCube = false;

        /// <summary>
        /// The orientation to turn the face
        /// </summary>
        public TurnType Orientation { get; set; }

        /// <summary>
        /// The face to turn
        /// </summary>
        public CubeFaceType Face { get; set; }

        /// <summary>
        /// Indicates if all the cube has to be turned or only the face.
        /// </summary>
        public bool TurnCube
        {
            get { return _turnCube; }
            set { _turnCube = value; }
        }

    }
}
