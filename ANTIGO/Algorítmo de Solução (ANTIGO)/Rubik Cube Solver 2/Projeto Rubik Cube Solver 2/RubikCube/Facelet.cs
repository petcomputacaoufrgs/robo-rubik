using System;
using System.Collections.Generic;
using System.Text;
using System.Drawing;

namespace RubikCube
{
    /// <summary>
    /// Represents a square in the face of the cube
    /// </summary>
    public class CubeFacelet
    {
        /// <summary>
        /// Face that this facelet belongs
        /// </summary>
        public CubeFaceType Face { get; set; }

        /// <summary>
        /// Position in the face
        /// </summary>
        public CubeFaceletType Square { get; set; }

        /// <summary>
        /// Rectangle from the image captured
        /// </summary>
        public Rectangle Rectangle { get; set; }

        /// <summary>
        /// Color of the facelet
        /// </summary>
        public Color Color { get; set; }
    }
}
