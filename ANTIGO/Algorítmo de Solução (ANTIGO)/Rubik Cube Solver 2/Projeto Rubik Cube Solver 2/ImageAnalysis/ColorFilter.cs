using System;
using System.Collections.Generic;
using System.Text;
using AForge.Imaging.Filters;

namespace ImageAnalysis
{
    /// <summary>
    /// Class that holds informations of a color filter with object detection
    /// </summary>
    public class ColorFilter : ColorFiltering
    {
        /// <summary>
        /// The ID of the filter object
        /// </summary>
        public string FilterID { get; set; }

        /// <summary>
        /// The minimum width to consider as a object detected
        /// </summary>
        public int MinWidth { get; set; }

        /// <summary>
        /// The minimum height to consider as a object detected
        /// </summary>
        public int MinHeight { get; set; }

        /// <summary>
        /// The border color to highlight detected objetcs
        /// </summary>
        public System.Drawing.Color BorderColor { get; set; }
    }
}
